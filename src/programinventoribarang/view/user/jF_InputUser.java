    public void autoNumber(){
        try {
            String sql="SELECT COUNT(kode_user) FROM user";
            ResultSet res = koneksi.createStatement().executeQuery(sql);
            while(res.next()){
                if(res.first() == false){
                    txtKodeUser.setText("USR0000001");
                } else{
                    res.last();
                    int auto_num = res.getInt(1)+1;
                    String no = String.valueOf(auto_num);
                    int kode = no.length();
                    
                    for(int ex=0;ex<7 - kode;ex++){
                        no = "0"+no;
                    } txtKodeUser.setText("USR"+no);
                }
            }
            res.close();
        } catch (Exception e) {
            System.err.println(e);
        }
    }

