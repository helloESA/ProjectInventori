    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
       if(txtKodeUser.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Kolom Kode Tidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
       } else if(txtNamaUser.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Kolom Username Tidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
       } else if(txtPassword.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Kolom Password Tidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
       } else if(txtNamaLengkap.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Kolom Nama Tidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
       } else if(cbLevel.getSelectedIndex() == 0){
           JOptionPane.showMessageDialog(this, "Silahkan Pilih Level","Perhatian",JOptionPane.WARNING_MESSAGE);
       } else if(txtTelepon.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Kolom Telepon Tidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
       } else {
           user.insert(this);
           JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_btSimpanActionPerformed

